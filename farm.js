// LOAD DISEASE DETAILS
// ==============================

function loadDiseaseDetails(){

    const params =
    new URLSearchParams(
        window.location.search
    );

    const crop =
    params.get("crop");

    const diseaseContainer =
    document.querySelector(
        ".disease-container"
    );

    if(
        !crop ||
        !diseaseDatabase[crop]
    ){

        diseaseContainer.innerHTML =
        `
        <h2>
        No disease data found
        </h2>
        `;

        return;
    }

    diseaseContainer.innerHTML = "";

    diseaseDatabase[crop].forEach(
    disease=>{

        diseaseContainer.innerHTML +=
        `

        <div class="disease-card">

            <div class="disease-image">

                <img
                src="${disease.image}"
                alt="${disease.title.english}">

            </div>

            <div class="disease-details">

                <h2>

                    ${
                    disease.title[currentLanguage]
                    }

                </h2>

                <p>

                    <strong>Cause:</strong>

                    ${
                    disease.cause[currentLanguage]
                    }

                </p>

                <p>

                    <strong>Symptoms:</strong>

                    ${
                    disease.symptoms[currentLanguage]
                    }

                </p>

                <p>

                    <strong>Treatment:</strong>

                    ${
                    disease.treatment[currentLanguage]
                    }

                </p>

                <p>

                    <strong>Prevention:</strong>

                    ${
                    disease.prevention[currentLanguage]
                    }

                </p>

            </div>

            <div class="disease-video">

                <video
                controls
                autoplay
                muted
                loop>

                    <source
                    src="${disease.video}"
                    type="video/mp4">

                </video>

            </div>

        </div>

        `;

    });

}

